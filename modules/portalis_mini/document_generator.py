def generate_port_checklist_txt(portalis_root, state) -> str:
    port_name = (state.voyage.arrival_port or "").strip()
    port = load_port_requirement(portalis_root, port_name) if port_name else {
        "port_name": "",
        "country": "",
        "required_docs": [],
        "non_standard_forms": [],
        "certificate_requirements": [],
        "notes": "",
    }

    lines = [
        "PORTALIS MINI - PORT REQUIREMENT CHECKLIST",
        f"Generated (UTC): {utc_now_iso()}",
        "-" * 72,
        f"Vessel: {state.vessel.name}",
        f"Arrival Port: {port.get('port_name', port_name)}",
        f"Country: {port.get('country', '')}",
        "-" * 72,
        "REQUIRED DOCUMENTS",
    ]

    if port.get("required_docs"):
        lines.extend(f"[ ] {item}" for item in port["required_docs"])
    else:
        lines.append("[ ] No required document list defined")

    lines.append("")
    lines.append("NON-STANDARD FORMS")
    if port.get("non_standard_forms"):
        lines.extend(f"[ ] {item}" for item in port["non_standard_forms"])
    else:
        lines.append("[ ] None listed")

    lines.append("")
    lines.append("CERTIFICATE REQUIREMENTS")
    if port.get("certificate_requirements"):
        lines.extend(f"[ ] {item}" for item in port["certificate_requirements"])
    else:
        lines.append("[ ] None listed")

    if port.get("notes"):
        lines.extend(["", "NOTES", port["notes"]])

    lines.append("-" * 72)
    return "\n".join(lines) + "\n"


def save_generated_text(portalis_root, category: str, filename: str, text: str) -> str:
    out_dir = Path(portalis_root) / "generated" / _safe_name(category)
    out_dir.mkdir(parents=True, exist_ok=True)

    out_path = out_dir / filename
    out_path.write_text(text, encoding="utf-8")
    return str(out_path)
