def generate_port_checklist_txt(portalis_root, state) -> str:
    port_name = (state.voyage.arrival_port or "").strip()
    port = load_port_requirement(portalis_root, port_name) if port_name else {
        "port_name": "",
        "country": "",
        "required_docs": [],
        "non_standard_forms": [],
        "certificate_requirements": [],
        "notes": "",
    }

    lines = []
    lines.append("PORTALIS MINI – PORT REQUIREMENT CHECKLIST")
    lines.append(f"Generated (UTC): {utc_now_iso()}")
    lines.append("-" * 72)
    lines.append(f"Vessel: {state.vessel.ship_name}")
    lines.append(f"Arrival Port: {port.get('port_name', port_name)}")
    lines.append(f"Country: {port.get('country', '')}")
    lines.append("-" * 72)

    lines.append("REQUIRED DOCUMENTS")
    if port.get("required_docs"):
        for x in port["required_docs"]:
            lines.append(f"[ ] {x}")
    else:
        lines.append("[ ] No required document list defined")

    lines.append("")
    lines.append("NON-STANDARD FORMS")
    if port.get("non_standard_forms"):
        for x in port["non_standard_forms"]:
            lines.append(f"[ ] {x}")
    else:
        lines.append("[ ] None listed")

    lines.append("")
    lines.append("CERTIFICATE REQUIREMENTS")
    if port.get("certificate_requirements"):
        for x in port["certificate_requirements"]:
            lines.append(f"[ ] {x}")
    else:
        lines.append("[ ] None listed")

    if port.get("notes"):
        lines.append("")
        lines.append("NOTES")
        lines.append(port["notes"])

    lines.append("-" * 72)
    return "\n".join(lines) + "\n"


def save_generated_text(portalis_root, category: str, filename: str, text: str) -> str:
    out_dir = Path(portalis_root) / "generated" / category
    out_dir.mkdir(parents=True, exist_ok=True)

    out_path = out_dir / filename
    out_path.write_text(text, encoding="utf-8")
    return str(out_path)