from __future__ import annotations

from pathlib import Path

from .crew_service import list_crew
from .port_requirements import load_port_requirement
from .storage import utc_now_iso


def _safe_name(value: str) -> str:
    return (
        (value or "")
        .strip()
        .lower()
        .replace(" ", "_")
        .replace("/", "_")
        .replace("\\", "_")
    )


def generate_crew_list_txt(portalis_root, state) -> str:
    crew_summary = list_crew(portalis_root)

    lines = [
        "PORTALIS MINI - CREW LIST",
        f"Generated (UTC): {utc_now_iso()}",
        "-" * 72,
        f"Vessel: {state.vessel.name}",
        f"IMO: {state.vessel.imo_number or ''}",
        f"Voyage No: {state.voyage.voyage_number}",
        f"Arrival Port: {state.voyage.arrival_port}",
        "-" * 72,
        f"{'No.':<4} {'Name':<30} {'Rank':<20} {'Crew ID':<12}",
        "-" * 72,
    ]

    for idx, crew in enumerate(crew_summary, start=1):
        rank = str(crew.get("rank") or "")
        name = str(crew.get("name") or "")
        lines.append(f"{idx:<4} {name[:30]:<30} {rank[:20]:<20} {crew['crew_id']:<12}")

    lines.append("-" * 72)
    lines.append(f"Total Crew: {len(crew_summary)}")
    return "\n".join(lines) + "\n"


