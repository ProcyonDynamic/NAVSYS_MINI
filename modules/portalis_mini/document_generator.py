from __future__ import annotations

from datetime import datetime, timezone
from pathlib import Path

from .crew_service import list_crew, load_crew_record
from .port_requirements import load_port_requirement


def utc_now_iso() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _safe_name(s: str) -> str:
    return (
        (s or "")
        .strip()
        .lower()
        .replace(" ", "_")
        .replace("/", "_")
        .replace("\\", "_")
    )


def generate_crew_list_txt(portalis_root, state) -> str:
    crew_summary = list_crew(portalis_root)

    lines = []
    lines.append("PORTALIS MINI – CREW LIST")
    lines.append(f"Generated (UTC): {utc_now_iso()}")
    lines.append("-" * 72)
    lines.append(f"Vessel: {state.vessel.ship_name}")
    lines.append(f"IMO: {state.vessel.imo}")
    lines.append(f"Voyage No: {state.voyage.voyage_no}")
    lines.append(f"Arrival Port: {state.voyage.arrival_port}")
    lines.append("-" * 72)
    lines.append(f"{'No.':<4} {'Name':<30} {'Rank':<20} {'Crew ID':<12}")
    lines.append("-" * 72)

    for i, c in enumerate(crew_summary, start=1):
        lines.append(f"{i:<4} {c['name'][:30]:<30} {c['rank'][:20]:<20} {c['crew_id']:<12}")

    lines.append("-" * 72)
    lines.append(f"Total Crew: {len(crew_summary)}")
    return "\n".join(lines) + "\n"


